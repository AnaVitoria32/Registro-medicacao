    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String nomePaciente=jTextField1.getText();
            String nomeMedicamento=jTextField2.getText();
            String dosagem=jTextField3.getText();
            LocalTime ultimaDose=LocalTime.parse(jFormattedTextField1.getText());
            ultimaDose = ultimaDose.minusMinutes(1); 
            int frequencia=Integer.parseInt(jTextField4.getText());
            int duracao=Integer.parseInt(jTextField5.getText());
            
            System.out.println("nomePaciente: " + nomePaciente);
            System.out.println("nomeMedicamento: " + nomeMedicamento);
            System.out.println("dosagem: " + dosagem);
            System.out.println("ultimaDose: " + ultimaDose);
            System.out.println("frequencia: " + frequencia);
            System.out.println("duracao: " + duracao);
            
            MedicamentoComum medi=new MedicamentoComum(nomePaciente, nomeMedicamento, dosagem, ultimaDose, frequencia, duracao);
            DadosApp.listaTodosMedicamentos.add(medi);
            
            Connection conn=ConexaoBD.conectar();
            if (conn == null) {
              JOptionPane.showMessageDialog(this, "Erro: Conex√£o com o banco falhou.");
              return;
            }
            Statement stmtInfo = conn.createStatement();
            ResultSet rs = stmtInfo.executeQuery("SELECT DATABASE()");
            if (rs.next()) {
             System.out.println("üì¢ Banco conectado: " + rs.getString(1));
            }
            conn.setAutoCommit(false);
            String sql="INSERT INTO medicamento_comum(nome_paciente, nome_medicamento, dosagem, ultima_dose,frequencia, duracao) VALUES(?,?,?,?,?,?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, nomePaciente);
            stmt.setString(2, nomeMedicamento);
            stmt.setString(3, dosagem);
            stmt.setTime(4, java.sql.Time.valueOf(ultimaDose));
            stmt.setInt(5, frequencia);
            stmt.setInt(6, duracao);
            stmt.executeUpdate( );
            conn.commit();
            
            stmt.close();
            conn.close( );
            
            JOptionPane.showMessageDialog(this, "O medicamento comum foi registrado com sucesso!");
            // Vai fazer a limpeza dos campos quando as informa√ß√µes forem salvas.
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField1.requestFocus();
        }catch(DateTimeParseException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "O hor√°rio informado est√° incorreto, tente no formato HH:mm. Exemplo 18:30.");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "A frequ√™ncia e dura√ß√£o devem ser em formato inteiro, sem pontos ou v√≠rgulas(, ou .)");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "N√£o foi poss√≠vel cadastrar o medicamento: "+e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

